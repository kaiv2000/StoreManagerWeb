package com.kaiv.model;

public class ReportObject {

    String productType;
    String activeQuantity;
    String activeMoneySum;

    public String getProductType() {
        return productType;
    }

    public void setProductType(String productType) {
        this.productType = productType;
    }

    public String getActiveQuantity() {
        return activeQuantity;
    }

    public void setActiveQuantity(String activeQuantity) {
        this.activeQuantity = activeQuantity;
    }

    public String getActiveMoneySum() {
        return activeMoneySum;
    }

    public void setActiveMoneySum(String activeMoneySum) {
        this.activeMoneySum = activeMoneySum;
    }
}
